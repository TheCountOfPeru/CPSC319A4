
public class Vertex {
	private int name;
	private int weight;
	public Vertex(int name, int weight) {
		super();
		this.name = name;
		this.weight = weight;
	}
	public int getName() {
		return name;
	}
	public void setName(int name) {
		this.name = name;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	
}
